# การสร้างตัวแปรเก็บค่าที่เป็น float
c = 0.5
d = 3/4
# ใช้ type ในการตรวจสอบประเภท
print(type(c))

# Boolean ค่าทางตรรกศาสตร์ (จริง,เท็จ)
t = True
f = False
print(type(f))

# Complex Number
# จากตัวแปร z แบ่งเป็น 2 ส่วน เลข 1 คือ จำนวนจริง 2j โดยตัว j คือจำนวนจินตภาพ
z = 1 + 2j
# ใช้ .real เมื่อต้องการแสดงจำนวนจริง (1)
print(z.real)
# แสดงเลขก่อนจำนวนจินตภาพ(j)
# result = 2
print(z.imag)

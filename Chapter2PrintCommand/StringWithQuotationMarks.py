# String with Quotation Marks

# String with Quote
# ถ้าต้องการพิมพ์อักขระที่มีเครื่องหมาย quote ให้ใช้ "..."
print("It's me")
# ถ้าต้องการใช้ '...' ต้องเติม \ ไว้ที่หน้า ' ในข้อความของเรา
print('It\'s me')

# String with Doublequote
print('He said "Python is my life."')
print("He said \"Python is my life.\"")

# String with Backslash
print('/\\')
print('/\\/\\/\\')

